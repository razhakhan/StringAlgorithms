class Solution:
    def Anagrams(self, words, n):
        '''
        words: list of word
        n:      no of words
        return : list of group of anagram {list will be sorted in driver code (not word in grp)}
        '''
        dic={}
        for word in words:
            sword=sorted(word)
            sword="".join(sword)
            if sword in dic:
                dic[sword].append(word)
            else:
                dic[sword]=[word]
        
        res=[]
        for i in dic:
            res.append(dic[i])
        return res
